static float normalize(float val) {
    return val * 0.35014087f;
}

void phase_init(phase_gen_t *p) {
    p->phase_accum = 0.0f;
    p->freq_step = 1.0f;
}

void phase_advance(phase_gen_t *p) {
    p->phase_accum += p->freq_step;
    if (p->phase_accum >= 200.0f) {
        p->phase_accum -= 200.0f;
    }
}

void phase_set_freq(phase_gen_t *p, float hz) {
    p->freq_step = hz * 200.0f / 10000.0f;
}

float phase_sin(const phase_gen_t *p) {
    int idx = (int)p->phase_accum;
    if (idx >= 200) idx = 0;
    return normalize(sin_table[idx]);
}

float phase_cos(const phase_gen_t *p) {
    int idx = ((int)p->phase_accum + 50) % 200;
    return normalize(sin_table[idx]);
}